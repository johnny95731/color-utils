/**
 * Find the closet named-color.
 */
export const rgb2named = (
  rgb: number[]
): string => {
  let minDist = Infinity;
  let dist: number;
  let closest;
  for (const [name, namedNgb] of namedColor) {
    dist = l2Dist3(rgb, namedNgb);
    if (dist < minDist) {
      closest = name;
      minDist = dist;
    }
  }
  // @ts-expect-error `minDist` is init to be `Infinity`. Thus closest will be assigned value.
  return closest;
};

export const named2rgb = (name: string) => {
  return namedColor.get(name) ?? [0, 0, 0];
};
